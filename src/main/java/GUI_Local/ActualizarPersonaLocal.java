    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String uid = jTextField1.getText();
        String nombre = jTextField2.getText();
        String apellido = jTextField3.getText();
        String direccion = jTextField4.getText();
        String cedula = jTextField5.getText();
        String producto = jTextField6.getText();
        String nombreimg = jTextField7.getText();

        int res, res1, res2, res3, res4, res5;

        res = Helpers.HelperValidacion.ValidarTodo(nombre);
        res1 = Helpers.HelperValidacion.ValidarTodo(apellido);
        res2 = Helpers.HelperValidacion.ValidarTodoLetra(cedula);
        res3 = Helpers.HelperValidacion.ValidarTodoDireccion(direccion);
        res4 = Helpers.HelperValidacion.ValidarTodoSerial(nombreimg);
        res5 = Helpers.HelperValidacion.ValidarTodo(uid);

        if (res == 1 && res1 == 1 && res2 == 1 && res3 == 1 && res4 == 1 && res5 == 1) {

            jTextField2.setBorder(new LineBorder(Color.RED, 1));
            jTextField1.setBorder(new LineBorder(Color.RED, 1));
            jTextField3.setBorder(new LineBorder(Color.RED, 1));
            jTextField4.setBorder(new LineBorder(Color.RED, 1));
            jTextField5.setBorder(new LineBorder(Color.RED, 1));
            jTextField7.setBorder(new LineBorder(Color.RED, 1));
            JOptionPane.showMessageDialog(null, "Error ingrese informaci√≥n valida en el formulario");
        } else {

            try {
                Persona per = new Persona(uid, nombre, apellido, direccion, cedula, producto, nombreimg);
                PersonaDAO dao = new PersonaDAO();
                dao.update(per);

                //String consulta="INSERT INTO `persona`(`Uid`, `Nombre`, `Apellido`, `Direccion`, `Cedula`, `Producto`, `Nom_img`) VALUES ('"+uid+"','"+nombre+"','"+apellido+"','"+direccion+"','"+cedula+"','"+producto+"','"+nombreimg+"')";
                JOptionPane.showMessageDialog(null, "Actualizaci√≥n Exitosa");
            } catch (Exception e) {
                System.out.println("Excepcion" + e);
            }

            MenuLocal menu = new MenuLocal();
            menu.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

